package com.Petal;

public class patientdatareader {
    private String hin;
    private String hinSequence;
    private String firstName;
    private String lastName;
    private String birthDate;
    private String gender;
    private String language;

    // Default constructor
    public patientdatareader() {}

    // Getters and setters
    public String getHin() { return hin; }
    public void setHin(String hin) { this.hin = hin; }
    public String getHinSequence() { return hinSequence; }
    public void setHinsequence(String hinSequence) { this.hinSequence = hinSequence; }
    public String getLastName() { return lastName; }
    public void setLastName(String lastName) { this.lastName = lastName; }
    public String getFirstName() { return firstName; }
    public void setFirstName(String firstName) { this.firstName = firstName; }
    public String getBirthDate() { return birthDate; }
    public void setBirthDate(String birthDate) { this.birthDate = birthDate; }
    public String getGender() { return gender; }
    public void setGender(String gender) { this.gender = gender; }
    public String getLanguage() { return language; }
    public void setLanguage(String language) { this.language = language; }

}


